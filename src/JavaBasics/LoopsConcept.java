package JavaBasics;

public class LoopsConcept {

	public static void main(String[] args) {
		
		//1. Whileloop
		
		int i=1; //initialization
		while (i<=20){ // conditional
			System.out.println(i);
			i=i+1; // incremental / decremental
		}		
	
		System.out.println("***********************");
		
		//2.for Loop
		// print 1 to 10
		//j++ means j+1
		for(int j=1;j<=15;j++){
			System.out.println(j);
		}
		
		System.out.println("@@@@@@@@@@@@@@@@");
		
		// print 10 to 1
		for(int k=10;k>=1;k--){
			System.out.println(k);
		}
		
		
		

	}

}
